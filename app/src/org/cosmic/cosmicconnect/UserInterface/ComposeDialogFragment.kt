package org.cosmic.cosmicconnect.UserInterface

import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import androidx.compose.runtime.Composable
import androidx.compose.ui.platform.ComposeView
import androidx.compose.ui.platform.ViewCompositionStrategy
import androidx.fragment.app.DialogFragment
import org.cosmic.cosmicconnect.UserInterface.compose.CosmicTheme

/**
 * Base class for DialogFragments that use Jetpack Compose for their UI.
 */
abstract class ComposeDialogFragment : DialogFragment() {

    @Composable
    abstract fun DialogContent()

    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View {
        return ComposeView(requireContext()).apply {
            setViewCompositionStrategy(ViewCompositionStrategy.DisposeOnViewTreeLifecycleDestroyed)
            setContent {
                CosmicTheme(context = context) {
                    DialogContent()
                }
            }
        }
    }
}
